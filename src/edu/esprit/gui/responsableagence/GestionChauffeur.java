    private void ButtonAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonAjouterActionPerformed

        Chauffeur c=new Chauffeur();
        
        c.setNumPermis(numPermis.getText());
        if (etatDispo.isSelected())
            c.setEtat(true);
        else if (etatNDispo.isSelected())
            c.setEtat(false);
        
        c.setLogin(login.getText());
        c.setPwd(password.getText());
        c.setNom(nom.getText());
        c.setPrenom(prenom.getText());
        c.setEmail(email.getText());
        c.setTelephone(Integer.parseInt(telephone.getText()));
        c.setAdresse(adresse.getText());
        c.setCin(Long.parseLong(cin.getText()));

        ChauffeurDAO cDAO =ChauffeurDAO.getInstance();
        cDAO.insertChauffeur(c);
        
        updateModel();
    }//GEN-LAST:event_ButtonAjouterActionPerformed

    private void ButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonUpdateActionPerformed
        Chauffeur c=new Chauffeur();
        
        c.setNumPermis(numPermis.getText());
        if (etatDispo.isSelected())
            c.setEtat(true);
        else if (etatNDispo.isSelected())
            c.setEtat(false);
        
        c.setLogin(jTable1.getModel().getValueAt(jTable1.getSelectedRow(),1).toString());
        c.setNom(nom.getText());
        c.setPrenom(prenom.getText());
        c.setEmail(email.getText());
        c.setTelephone(Integer.parseInt(telephone.getText()));
        c.setAdresse(adresse.getText());
        c.setCin(Long.parseLong(cin.getText()));
        
        ChauffeurDAO.getInstance().updateChauffeur(c);
        updateModel();
    }//GEN-LAST:event_ButtonUpdateActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
            
            cin.setText(jTable1.getModel().getValueAt(jTable1.getSelectedRow(),2).toString());
            numPermis.setText(jTable1.getModel().getValueAt(jTable1.getSelectedRow(),3).toString());
            nom.setText(jTable1.getModel().getValueAt(jTable1.getSelectedRow(),4).toString());
            prenom.setText(jTable1.getModel().getValueAt(jTable1.getSelectedRow(),5).toString());
            telephone.setText(jTable1.getModel().getValueAt(jTable1.getSelectedRow(),6).toString());
            email.setText(jTable1.getModel().getValueAt(jTable1.getSelectedRow(),7).toString());
            adresse.setText(jTable1.getModel().getValueAt(jTable1.getSelectedRow(),8).toString());
            if ("false".equals(jTable1.getModel().getValueAt(jTable1.getSelectedRow(),9).toString())){
                etatNDispo.setSelected(true);
                etatDispo.setSelected(false);
            }
            else if ("true".equals(jTable1.getModel().getValueAt(jTable1.getSelectedRow(),9).toString())){
                etatDispo.setSelected(true);
                etatNDispo.setSelected(false);
            }
    }//GEN-LAST:event_jTable1MouseClicked

