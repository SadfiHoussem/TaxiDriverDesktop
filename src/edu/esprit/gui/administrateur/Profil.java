    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        String nomAdmin = nom.getText();
	if (!Pattern.matches("[a-zA-Z]*", nomAdmin ))
	{
		JOptionPane.showMessageDialog(null, "Rentrez un nom valide" );
		return;
        }
        
        String prenomAdmin = prenom.getText();
        if (!Pattern.matches("[a-zA-Z]*", prenomAdmin))
	{
		JOptionPane.showMessageDialog(null, "Rentrez un prenom valide" );
		return;
        }
        
        
        
        if (nom.getText().isEmpty()
                | prenom.getText().isEmpty()
                | cin.getText().isEmpty()
                | adresse.getText().isEmpty()
                | tel.getText().isEmpty()
                | email.getText().isEmpty()
                | mdp.getText().isEmpty()
                | login.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "veuiller remplir tous les champs ", "Champ vide", JOptionPane.ERROR_MESSAGE);
        
            
       
        } 
        
        
        
        else {
            
            try {

                int acin = Integer.parseInt(this.cin.getText());
                
                 if ( !(cin.getText().length() == 8 )){{
                            JOptionPane.showMessageDialog(null, "Rentrer une CIN de 8 chiffres" );
		return;
        }
                        }
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Le champ CIN n'accepte que les chiffres ", "Alert", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            try {
                int numtel = Integer.parseInt(this.tel.getText());
                 
                        if ( !(tel.getText().length() == 8 )){{
                            JOptionPane.showMessageDialog(null, "Rentrer un numero de telephone de 8 chiffres" );
		return;
        }
                        }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Le champ Numero de telephone n'accepte que les chiffres ", "Alert", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            String mail = email.getText();
         if ((!mail.contains("@"))&&(!mail.contains("."))) {
         {
		JOptionPane.showMessageDialog(null, "Rentrez un mail valide" );
		return;
        }}
            
       admin.setLogin(login.getText());
        admin.setNom(nom.getText());
        admin.setPrenom(prenom.getText());
         admin.setPwd(mdp.getText());
       admin.setEmail(email.getText());
        admin.setTelephone(Integer.parseInt(tel.getText()));
        admin.setAdresse(adresse.getText());
        admin.setCin(Long.parseLong(cin.getText()));

        AdministrateurDAO aDAO = AdministrateurDAO.getInstance();
        aDAO.updateAdministrateur(admin);
       // AccueilChauffeur.setChauffeur(c);
        JOptionPane.showMessageDialog(this, "Modification reusie");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

