    private void ButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonUpdateActionPerformed

      Agence g =new Agence();
      
      g.setIdAgence(Integer.parseInt(jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0).toString()));
      
      g.setNomAgence(nom.getText());
      
      String aux=idresp.getSelectedItem()+"";

      ResponsableAgence r=ResponsableAgenceDAO.getInstance().findRespAgenceById(Integer.parseInt(aux.substring(0, aux.indexOf(":"))));    
      g.setRespAgence(r);
            
      g.setTelAgence(Integer.parseInt(tel.getText()));
      g.setAdresseAgence(adresse.getText());
      
      AgenceDAO.getInstance().updateAgence(g);
        updateModel();
    }//GEN-LAST:event_ButtonUpdateActionPerformed

