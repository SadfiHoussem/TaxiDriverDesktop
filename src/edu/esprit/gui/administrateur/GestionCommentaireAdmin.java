private void UpdateModelCommentaire()
    {
          DefaultTableModel model = (DefaultTableModel) tblCommentaire.getModel();
          DefaultTableModel model2 = (DefaultTableModel) tblOffre.getModel();

     try
     {
        CommentaireDAO com=CommentaireDAO.getInstance();
        ResultSet listCommentaire;
        int idOffre=Integer.parseInt((model2.getValueAt(tblOffre.getSelectedRow(),0)).toString());
       listCommentaire   = com.DisplayAllCommentaires2(idOffre);
             
        tblCommentaire.setModel(DbUtils.resultSetToTableModel(listCommentaire));
     }
     catch (Exception e)
     {
         JOptionPane.showMessageDialog(null, e);
              
     }

    }
