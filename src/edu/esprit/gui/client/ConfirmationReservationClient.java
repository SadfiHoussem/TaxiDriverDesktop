    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Trajet trajet=new Trajet();
        trajet.setAdresseDep(depart);
        trajet.setAdresseDest(dest);
        trajet.setAgence(AgenceDAO.getInstance().findAgenceById(idAgence));
        trajet.setEtat(true);
        
        TrajetDAO.getInstance().insertTrajet(trajet);
        int idTrajet= TrajetDAO.getInstance().findIdTrajet(dest, depart);

        Client client=new Client();
        client.setIdClient(idClient);
        Taxi taxi=TaxiDAO.getInstance().findTaxiById(idTaxi);        
        Agence agence=AgenceDAO.getInstance().findAgenceById(idAgence);
        Trajet trajet2 =TrajetDAO.getInstance().findTrajetById(idTrajet);
        Reservation res=new Reservation();
        
        res.setAgence(agence);
        res.setTaxi(taxi);
        res.setClient(client);
        res.setTrajet(trajet2);
        res.setDate(date);
        res.setNote(0);
        res.setConfirme(false);
        
        ReservationDAO.getInstance().insertReservation(res);
        
        ConsulterOffre_1 pageAccAgence=new ConsulterOffre_1(login,pwd,idClient);
        pageAccAgence.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

