    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed

        if (t1.getText().isEmpty()
                | t2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "veuiller remplir tous les champs ", "Champ vide", JOptionPane.ERROR_MESSAGE);
        }else{
            
            Client client=new Client();
            client.setIdClient(idClient);
            Agence agence=new Agence();
            agence.setIdAgence(idAgence);
            Reclamation rec=new Reclamation();

            rec.setClient(client);
            rec.setAgence(agence);
            rec.setsujetReclamation(t1.getText());
            rec.setcontenu(t2.getText());
            rec.setEtat(false);

            ReclamationDAO.getInstance().insertReclamation(rec);
            ConsulterOffre_1 page=new ConsulterOffre_1(log, pwd,idClient);
            page.setVisible(true);
            this.setVisible(false);
            }
    }//GEN-LAST:event_button1ActionPerformed

